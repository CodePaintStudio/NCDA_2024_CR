function changeBigSize(ele, length) {
    if (length * 10 / 5 >= 65) {
        length = 65
        ele.style.width = `${length}vh`
    } else if (length * 5 / 5 <= 40) {
        length = 40
        ele.style.width = `${length}vh`
    } else {
        length *= 5
        ele.style.width = `${length}vh`
    }
    return length
}

