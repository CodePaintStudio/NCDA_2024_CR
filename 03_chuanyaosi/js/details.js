function addPanels(nowHerbIds, nowCity) {
    // const nowHerbId = nowHerbIds
    panels = document.querySelectorAll('.manyWindows .panel')
    panels.forEach((panel, index) => {
        // console.log(22323);
        const listener = () => updatePanels(panel, index, nowCity, nowHerbIds)
        panel.addEventListener('click', listener)
        listenersMap.set(panel, listener);
    })
}

function updatePanels(panel, index, nowCity, nowHerbId) {
    let contentBox = panel.querySelector('.contentBox')
    let vertical = panel.querySelector('.manyWindows .vertical')
    if (panel.classList.contains('active') && contentBox) {
        vertical.style.opacity = 0
        vertical.style.visibility = 'hidden'
        panel.removeChild(contentBox)
        panel.classList.remove('active')

    } else {
        removeActiveClass()
        panel.classList.add('active')
        contentBox = document.createElement('div')
        contentBox.classList.add('contentBox')
        setTimeout(() => {
            vertical.style.opacity = 1
            vertical.style.visibility = 'visible'
            contentBox.innerHTML = nowCity.herbs[nowHerbId].literature[index].content
        }, 100);
        panel.appendChild(contentBox)
        // console.log(1212);
    }
}

function removeActiveClass() {
    panels.forEach(panel => {
        let contentBox = panel.querySelector('.contentBox')
        let vertical = panel.querySelector('.manyWindows .vertical')
        if (contentBox) {
            panel.removeChild(contentBox)
            vertical.style.opacity = 0
            vertical.style.visibility = 'hidden'
        }
        panel.classList.remove('active')
    })
}
